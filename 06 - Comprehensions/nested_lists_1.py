listas = [[1, 2, 3], [4, 5, 6], [7, 8, 9]] # Matriz 3x3

print("----------------------------------------------------------------------------------------------------")

print(listas[0]) # [1, 2, 3]
print(listas[1]) # [4, 5, 6]
print(listas[2]) # [7, 8, 9]

print("----------------------------------------------------------------------------------------------------")

print(listas[0] [1]) # 2
print(listas[1] [2]) # 6
print(listas[2] [0]) # 8

print("----------------------------------------------------------------------------------------------------")


# Iterando com loops em listas aninhadas

for lista in (listas):
    print(lista)

print("----------------------------------------------------------------------------------------------------")

for lista in (listas):
    for numero in lista:
        print(numero)

print("----------------------------------------------------------------------------------------------------")
