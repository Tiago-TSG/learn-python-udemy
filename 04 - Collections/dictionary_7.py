receita = dict(Janeiro = "R$100,00", Fevereiro = "R$350,00", Março = "R$750,00", Abril = "R$1020,00", Maio = "R$2300,00")

print(receita.keys())

print("----------------------------------------------------------------------------------------------------")

for chave in receita.keys(): # Modo mais recomendado.
    print(receita[chave])

print("----------------------------------------------------------------------------------------------------")

print(receita.values())

print("----------------------------------------------------------------------------------------------------")

for values in receita.values():
    print(values)

print("----------------------------------------------------------------------------------------------------")

print(receita.items())

print("----------------------------------------------------------------------------------------------------")

for key, value in receita.items():
    print(f"Chave = {key} e Valor = {value}")

print("----------------------------------------------------------------------------------------------------")

