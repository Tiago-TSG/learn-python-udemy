lista = [10,20,30,40,50,60]

print("----------")

print(lista[5])

print(lista[(len(lista) - 1)]) # O último elemento da lista é também o tamanho da lista -1 (entre colchetes).
print(lista[-1]) # O último elemento da lista pode ser lido de trás pra frente atrás de um índice -1.

print("----------")

print(lista[3:5]) # inclui o primeiro elementro do intervalo, mas exclui o último.
print(lista[3:]) # inclui o primeiro elementro do intervalo e todos os outros até o fim da lista.
